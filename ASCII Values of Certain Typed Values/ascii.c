#include <stdio.h>

int main(){
    char a = 'A';
    char b = 67;
    char c = 97;

    printf("Character Value of a : %c\n", a);
    printf("ASCII Value of a : %d\n\n", a);

    printf("Character Value of b : %c\n", b);
    printf("ASCII Value of b : %d\n\n", b);

    printf("Character Value of c : %c\n", c);
    printf("ASCII Value of c : %d", c);

    return 0;
}